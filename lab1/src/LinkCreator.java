/**
 * Bean that keeps the string that will be used to create a link.
 */
public class LinkCreator {
	private String str = "";

	/**
	 * Returns the string
	 * @return the string
	 */
	public String getStr() {
		return str;
	}

	/**
	 * Sets the string
	 * @param str new String
	 */
	public void setStr(String str) {
		this.str = str;
	}
}
